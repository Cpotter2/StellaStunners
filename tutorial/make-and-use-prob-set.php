    contains between 5 and 8 problems.  (Thus each of the eighteen sets contains between twenty-five and forty problems, most usually thirty-five.)</p>

  <p>To see any of the eighteen sets, just click on it.</p>

  <p>Suppose you are starting the year with your geometry class, and you want to find out what we suggest for a geometry class in set one.  Do these steps in order:</p>

  <p>1. Open the Problems menu.</p>

  <p>2. Click on Filter and click on geometry.</p>

  <p>3. Click on Add Tag.</p>

  <p>4. Click on Set One.  There they are.</p>


  <p>So, in order: Problems, Filter (choose the course), Tag, Set #.</p>

  <p>Once you’re that far, notice the red-outlined box: Make a Document with these problems.  Click on it and follow the directions and you’ll have that problem set on your desktop as a PDF.</p>

  <p>Going further, if you want to edit the set, you can browse around and find something that’s a better fit for what you want.  The browsing button will show you how.</p>

  <p>By the way, there are no problems that appear in more than one problem set.  You could give your students Stellas for five years, using the problem sets, and they would
    never see the same problem twice!  (But, thus, if you edit a problem set and stick in something else, you might find it turning up in another set later. The green tags that
    you’ll see with the problems will tip you off about this. Be ye warned.)</p>

  <p>Each problem comes with three green tags, indicating the set that it’s in, the course that it’s in, and what kind of a problem it is (among the 13 overall topics).</p>

  <p>The introductory problems are discussed under the browsing button. The discussion is all about . . . wait for it . . . heuristics.</p>

  <p>Finally, about set 99.  These are problems that aren't in any of the 18 official problem sets.  This is where we add more problems that we find.
    If you send us nifty problems of your own to add to the library (hint, hint), this is where they'll go.</p>

  <p>Why eighteen problem sets?  The idea is to give you enough to get you through the school year if you use one every two weeks or so. We have tried to arrange the content of
    these problem sets to coordinate, more or less, with the material being taught as the year goes along.</p>

  <p>Set 99 is the problems that are not in any of the eighteen sets of suggested problems.  That’s where we put additional problems that we want to add to the library.
    They are not tagged for any particular course.  As mentioned otherwhere, if you have a nifty problem for the library, send it to us!</p>

