    <p>In the case of “the problem of the week”, you might have a class discussion for reflecting on the problem once it’s solved—how students felt when they first saw it,
      how the class (or an individual) made progress toward a solution, and how they’re feeling now that it’s done (happy? angry?). This opens up the idea of lingering on
      a problem once it’s solved, teasing out further insights about it, finding alternate solutions, etc.</p>

    <p>One other way to enjoy the problems is to work through a fun one as a class on Friday afternoon--see
      <a href="http://stella.cs.oberlin.edu/problems/show-problem.php?id=1070.19">1070.19</a> as an example.</p>

    <p>Bottom line: beware of jumping in with all four feet too soon, especially as regards grading anxiety. Also, you might wait a few weeks to let the school year settle down before
      introducing Stella to the class. In the next essay, about how I started, you’ll read the result of my many years, starting in the 1960's, of toying around
      with these problems with students.</p>

